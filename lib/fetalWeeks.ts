// Return the most relevant week entry for a given pregnancy week (rounds down to
// the nearest entry we have content for).
export function getFetalWeek(week: number): FetalWeek | null {
  if (!week || week < 1) return null
  const sorted = [...FETAL_WEEKS].sort((a, b) => a.week - b.week)
  let match: FetalWeek | null = null
  for (const w of sorted) {
    if (w.week <= week) match = w
  }
  return match ?? sorted[0]
}
