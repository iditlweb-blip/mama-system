export async function streamChatResponse(
  messages: Array<{ role: 'user' | 'assistant'; content: string }>,
  mode: ChatMode
) {
  return anthropic.messages.stream({
    model: 'claude-sonnet-4-6',
    max_tokens: 1024,
    system: [
      {
        type: 'text',
        text: systemPrompts[mode],
        cache_control: { type: 'ephemeral' }
      }
    ],
    messages,
  })
}
